    private boolean isAdmin() {
        String aidSomKollas = null;
        try {
            String sqlFraga = "SELECT aid FROM admin WHERE aid = " + aid;
            aidSomKollas = idb.fetchSingle(sqlFraga);
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
        return aidSomKollas != null;
    }

    public void fyllCBLand() {
        try {
            String sqlFraga = "SELECT namn FROM land";
            System.out.println(sqlFraga);
            ArrayList<String> projektNamnLista = idb.fetchColumn(sqlFraga);

            for (String ettProjekt : projektNamnLista) {
                cbLand.addItem(ettProjekt);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

