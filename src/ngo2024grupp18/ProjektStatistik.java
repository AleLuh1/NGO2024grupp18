            String sqlFragaPagaendeProj = "SELECT SUM(kostnad) AS kostnadPagaende FROM projekt JOIN anstalld ON anstalld.aid = projekt.projektchef WHERE status = 'Pågående' AND anstalld.aid = " + aid + "";
